interface Dict {
  [key: string]: string | Dict;
}

function getNested(obj: Dict, path: string): string | undefined {
  const parts = path.split(".");
  let cur: string | Dict = obj;
  for (const p of parts) {
    if (typeof cur !== "object" || cur === null || !(p in cur)) return undefined;
    cur = cur[p] as string | Dict;
  }
  return typeof cur === "string" ? cur : undefined;
}

function interpolate(str: string, params?: Record<string, string | number>) {
  if (!params) return str;
  return Object.entries(params).reduce(
    (s, [k, v]) => s.replaceAll(`{${k}}`, String(v)),
    str,
  );
}

