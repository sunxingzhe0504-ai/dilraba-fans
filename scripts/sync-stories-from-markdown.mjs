/**
 * Parse Markdown posts with YAML frontmatter from content/posts/.
 * Run: node scripts/sync-stories-from-markdown.mjs
 * Hooked into prebuild — outputs content/stories-from-md.ts
 */
import { readFileSync, readdirSync, writeFileSync, existsSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const postsDir = join(root, "content", "posts");
const outFile = join(root, "content", "stories-from-md.ts");

function parseFrontmatter(raw) {
  const match = raw.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n([\s\S]*)$/);
  if (!match) return null;
  const meta = {};
  for (const line of match[1].split("\n")) {
    const m = line.match(/^(\w+):\s*(.+)$/);
    if (!m) continue;
    let val = m[2].trim();
    if (val.startsWith("[") && val.endsWith("]")) {
      meta[m[1]] = val
        .slice(1, -1)
        .split(",")
        .map((s) => s.trim().replace(/^["']|["']$/g, ""))
        .filter(Boolean);
    } else if (val === "true") meta[m[1]] = true;
    else if (val === "false") meta[m[1]] = false;
    else meta[m[1]] = val.replace(/^["']|["']$/g, "");
  }
  return { meta, body: match[2].trim() };
}

function escapeTemplate(str) {
  return str.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
}

function toStoryObject(meta, body) {
  const fields = [
    `slug: ${JSON.stringify(meta.slug)}`,
    `title: ${JSON.stringify(meta.title)}`,
    `date: ${JSON.stringify(meta.date)}`,
    `summary: ${JSON.stringify(meta.summary)}`,
    `body: \`${escapeTemplate(body)}\``,
  ];
  const optional = ["titleEn", "summaryEn", "cover", "eventSlug", "newsSlug", "magazineSlug", "workSlug"];
  for (const key of optional) {
    if (meta[key]) fields.push(`${key}: ${JSON.stringify(meta[key])}`);
  }
  if (meta.bodyEn) fields.push(`bodyEn: \`${escapeTemplate(meta.bodyEn)}\``);
  if (meta.tags?.length) fields.push(`tags: ${JSON.stringify(meta.tags)}`);
  if (meta.featured) fields.push(`featured: true`);
  return `  {\n    ${fields.join(",\n    ")}\n  }`;
}

function main() {
  if (!existsSync(postsDir)) {
    writeFileSync(outFile, `import type { Story } from "@/lib/types";\n\n/** Auto-generated — no Markdown posts yet. */\nexport const storiesFromMarkdown: Story[] = [];\n`);
    console.log("No content/posts/ — wrote empty stories-from-md.ts");
    return;
  }

  const files = readdirSync(postsDir).filter((f) => f.endsWith(".md"));
  const stories = [];

  for (const file of files) {
    const parsed = parseFrontmatter(readFileSync(join(postsDir, file), "utf8"));
    if (!parsed) {
      console.warn(`Skip ${file}: invalid frontmatter`);
      continue;
    }
    const { meta, body } = parsed;
    if (!meta.slug || !meta.title || !meta.date || !meta.summary) {
      console.warn(`Skip ${file}: missing required fields (slug, title, date, summary)`);
      continue;
    }
    stories.push(toStoryObject(meta, body));
  }

  const content = `/** Auto-generated by scripts/sync-stories-from-markdown.mjs — do not edit. */
import type { Story } from "@/lib/types";

export const storiesFromMarkdown: Story[] = [
${stories.join(",\n")}
];
`;

  writeFileSync(outFile, content);
  console.log(`Synced ${stories.length} story/stories from Markdown -> content/stories-from-md.ts`);
}

main();
